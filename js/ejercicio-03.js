// Ejercicio Numero 3: Escriba un programa que pida un número y diga si es divisible por 2.

//Verificación de Par o Impar
let numero = prompt('Ingrese cualquier número: ')

if((numero % 2) == 0){
    console.log(`El numero ${numero} es divisible.`);
    
}else{
    console.log(`El número ${numero} no es divisible.`);
    
}



